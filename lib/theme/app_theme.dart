  static TextTheme _textTheme({required bool isLight}) {
    final base = GoogleFonts.interTextTheme();
    final color = isLight ? textPrimaryLight : textPrimaryDark;
    final secondary = isLight ? textSecondaryLight : textSecondaryDark;
    return base.copyWith(
      displayLarge: base.displayLarge?.copyWith(color: color, fontWeight: FontWeight.w700),
      displayMedium: base.displayMedium?.copyWith(color: color, fontWeight: FontWeight.w700),
      displaySmall: base.displaySmall?.copyWith(color: color, fontWeight: FontWeight.w700),
      headlineLarge: base.headlineLarge?.copyWith(color: color, fontWeight: FontWeight.w700),
      headlineMedium: base.headlineMedium?.copyWith(color: color, fontWeight: FontWeight.w600),
      headlineSmall: base.headlineSmall?.copyWith(color: color, fontWeight: FontWeight.w600),
      titleLarge: base.titleLarge?.copyWith(color: color, fontWeight: FontWeight.w600),
      titleMedium: base.titleMedium?.copyWith(color: color, fontWeight: FontWeight.w600),
      titleSmall: base.titleSmall?.copyWith(color: color, fontWeight: FontWeight.w600),
      bodyLarge: base.bodyLarge?.copyWith(color: secondary, fontWeight: FontWeight.w400),
      bodyMedium: base.bodyMedium?.copyWith(color: secondary, fontWeight: FontWeight.w400),
      bodySmall: base.bodySmall?.copyWith(color: secondary, fontWeight: FontWeight.w400),
      labelLarge: base.labelLarge?.copyWith(color: color, fontWeight: FontWeight.w600),
      labelMedium: base.labelMedium?.copyWith(color: color, fontWeight: FontWeight.w600),
      labelSmall: base.labelSmall?.copyWith(color: color, fontWeight: FontWeight.w600),
    );
  }
